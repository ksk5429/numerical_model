"""
Auto-generate LaTeX tables for the dissertation (Task 11).

Reads the JSON artifacts produced by the V&V scripts and emits
standalone .tex files suitable for ``\\input{}`` from any Quarto /
LaTeX chapter. Running this script after a V&V sweep keeps the
dissertation draft in lock-step with the committed Op^3 outputs.

Tables produced
---------------
  calibration_regression_table.tex   Task 1.3 reference comparison
  dnv_conformance_table.tex          Task 4.4 audit
  iec_conformance_table.tex          Task 4.5 audit
  sensitivity_tornado_table.tex      Task 1.4 ranking
  pisa_cross_val_table.tex           Task 3.1e (AWAITING_VERIFY flagged)
  oc6_phase2_table.tex               Task 4.x (AWAITING_VERIFY flagged)

Run:
    python scripts/generate_latex_tables.py
    python scripts/generate_latex_tables.py --out PHD/tables/
"""
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
BENCH = REPO_ROOT / "validation/benchmarks"


def _esc(s: str) -> str:
    """Escape LaTeX specials conservatively."""
    return (str(s)
            .replace("\\", "\\textbackslash{}")
            .replace("&", "\\&")
            .replace("%", "\\%")
            .replace("_", "\\_")
            .replace("#", "\\#")
            .replace("$", "\\$"))


def calibration_table() -> str:
    p = BENCH / "calibration_regression.json"
    if not p.exists():
        return "% calibration_regression.json missing\n"
    rows = json.loads(p.read_text(encoding="utf-8"))
    lines = [
        "% Auto-generated by scripts/generate_latex_tables.py",
        "\\begin{table}[htbp]",
        "\\centering",
        "\\caption{Op\\textsuperscript{3} calibration regression against published reference frequencies.}",
        "\\label{tab:op3_calibration_regression}",
        "\\begin{tabular}{lrrrrc}",
        "\\toprule",
        "Example & $f_1^\\text{Op3}$ (Hz) & $f_1^\\text{ref}$ (Hz) & "
        "Error & Tol. & Status \\\\",
        "\\midrule",
    ]
    for r in rows:
        f = r["f1_hz"]
        ref = r["reference_hz"]
        err = r["error_fraction"] * 100
        tol = r["tolerance"] * 100
        name = _esc(r["example"].replace("_", " "))
        status = "\\checkmark" if r["status"] == "PASS" else "$\\times$"
        lines.append(
            f"{name} & {f:.4f} & {ref:.4f} & "
            f"{err:+.1f}\\% & $\\pm${tol:.0f}\\% & {status} \\\\"
        )
    lines.extend([
        "\\bottomrule",
        "\\end{tabular}",
        "\\end{table}",
    ])
    return "\n".join(lines) + "\n"


def dnv_table() -> str:
    p = BENCH / "dnv_st_0126_conformance.json"
    if not p.exists():
        return "% dnv_st_0126_conformance.json missing\n"
    data = json.loads(p.read_text(encoding="utf-8"))
    lines = [
        "% Auto-generated",
        "\\begin{table}[htbp]",
        "\\centering",
        "\\caption{DNV-ST-0126 conformance audit of Op\\textsuperscript{3} examples.}",
        "\\label{tab:dnv_conformance}",
        "\\begin{tabular}{llc}",
        "\\toprule",
        "Example & Clause & Status \\\\",
        "\\midrule",
    ]
    for ex in data:
        for ch in ex["checks"]:
            mark = {"PASS": "\\checkmark", "FAIL": "$\\times$",
                    "WARNING": "$\\triangle$",
                    "NOT_APPLICABLE": "--"}.get(ch["status"], "?")
            lines.append(
                f"{_esc(ex['example'])} & {ch['id']} {_esc(ch['title'])} & {mark} \\\\"
            )
        lines.append("\\midrule")
    lines[-1] = "\\bottomrule"
    lines.extend(["\\end{tabular}", "\\end{table}"])
    return "\n".join(lines) + "\n"


