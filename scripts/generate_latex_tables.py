def sensitivity_table() -> str:
    p = BENCH / "calibration_tornado_summary.json"
    if not p.exists():
        return "% calibration_tornado_summary.json missing\n"
    data = json.loads(p.read_text(encoding="utf-8"))
    lines = [
        "% Auto-generated",
        "\\begin{table}[htbp]",
        "\\centering",
        "\\caption{Op\\textsuperscript{3} first-mode sensitivity tornado.}",
        "\\label{tab:sensitivity_tornado}",
        "\\begin{tabular}{llrrr}",
        "\\toprule",
        "Example & Parameter & $-$10\\% & $+$10\\% & Swing \\\\",
        "\\midrule",
    ]
    for ex in data:
        for row in ex["rows"][:5]:   # top 5 per example
            lines.append(
                f"{_esc(ex['example'][:20])} & {_esc(row['parameter'])} & "
                f"{row['df_minus_pct']:+.2f}\\% & "
                f"{row['df_plus_pct']:+.2f}\\% & "
                f"{row['swing_pct']:+.2f}\\% \\\\"
            )
        lines.append("\\midrule")
    lines[-1] = "\\bottomrule"
    lines.extend(["\\end{tabular}", "\\end{table}"])
    return "\n".join(lines) + "\n"


